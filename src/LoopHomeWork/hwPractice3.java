package LoopHomeWork;



public class hwPractice3 {

    public static void main(String[] args) {

        int square=0;
        int sum=0;

        for (int i =1;i<=5 ;i++) {
            square=i*i;
            System.out.println(square);
            sum=sum+square;
        }

        System.out.println(sum);
    }


}
