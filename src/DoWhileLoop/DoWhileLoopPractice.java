package DoWhileLoop;

public class DoWhileLoopPractice {

    public static void main(String[] args) {


        int totalNum=4;


        do {
            System.out.println("Aimira");
            totalNum--;

        } while (totalNum>0);


        int totalNum1=0;

        do {
            System.out.println("AIMIRA");
            totalNum1--;

        } while (totalNum1>0);


    }



}
