package DoWhileLoop;

public class TaskDivide15 {

    public static void main(String[] args) {

        int num1 = 10;
        int num2 = 100;

        while (num1 <= num2) {
            if (num1 % 15 == 0) {

                System.out.println(num1);
            }
            num1++;
        }
    }
}




