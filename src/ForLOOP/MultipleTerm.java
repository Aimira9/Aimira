package ForLOOP;

public class MultipleTerm {

    public static void main(String[] args) {


        //Interview Question
       // What is the difference between defining the variables
        //and declaring variable
        //int number-->declaring the variable
        //number =10; --> defining the variable.Initialization


        int k=5;


        for(int i=0 ,p=6;k>1 && i<5;k--,i++) { // k 4 ,k 3 ,k 2
            //k>1 && i<5 -->if its true we will execute p

            System.out.println(p++); //6,7,8,9
        }





    }
}
