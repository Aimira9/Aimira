package ForLOOP;

public class TechtorialAcadLoop {

    public static void main(String[] args) {

        //example1

        for(int i=0 ;i<3;i++) {
            System.out.println("Techtorial");
            for(int k=0;k<4;k++) {

                System.out.println("Academy");
            }
        }
        //example2
        for(int i=0 ;i<3;i++){
            for(int k=0;k<4;k++) {
                System.out.println("Techtorial");
                System.out.println("Academy");
            }

        }

    }
}
