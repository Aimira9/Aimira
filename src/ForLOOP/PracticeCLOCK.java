package ForLOOP;

public class PracticeCLOCK {

    public static void main(String[] args) {

        //clock
        //bigger for min
        //small for hour

        for (int i = 2; i<=4; i++) {
            System.out.println(i);

            for (int k = 1; k <=60; k++) {
                System.out.print(k);
            }
            System.out.println();
        }
    }
}
